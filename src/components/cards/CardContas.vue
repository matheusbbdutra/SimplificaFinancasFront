<template>
  <div class="bg-gray-800 rounded-lg shadow-md p-6">
    <div class="flex items-center">
      <div class="bg-blue-500 p-3 rounded-full">
        <Users class="w-6 h-6 text-white" />
      </div>
      <div class="ml-4">
        <p class="text-sm text-gray-400">Contas</p>
        <p class="text-2xl font-bold text-white">{{ formatCurrency(totalContas) }}</p>
        <p class="text-sm text-gray-400">Previsto</p>
      </div>
    </div>
  </div>
</template>

<script setup>
import {computed, defineProps} from 'vue';
import { Users } from 'lucide-vue-next';

const props = defineProps({
  contas: {
    type: Array,
    required: true,
    default: () => []
  }
});

const totalContas = computed(() => {
  return props.contas.reduce((total, conta) => total + conta.saldoAtual, 0);
});

const formatCurrency = (value) => {
  return value.toLocaleString('pt-BR', { style: 'currency', currency: 'BRL' });
};
</script>
